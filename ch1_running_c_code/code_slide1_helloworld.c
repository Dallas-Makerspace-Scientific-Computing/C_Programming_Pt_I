#include <stdio.h>  // preprocessor directive to standard input/output standard library

int main()  // driver/main function: where the program always begins. The return type is "int", which means integer.
{
    printf("Hello, World!\n");  // "Print Hello, World!" to standard output.
    return 0;  // the function has return type int, so we must return an int. Usually, we want to return 0 to signal PROGRAM SUCCESS.
}